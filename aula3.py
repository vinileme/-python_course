# aspas simples
print('Vinicius Leme')
print('Vinicius "Leme"')


# aspas duplas
print("Vinicius Leme")
print("Vinicius 'Leme'")


# escape
print("Vinicius \"Leme\"")

# r
print(r"Vinicius \"Leme\"")

